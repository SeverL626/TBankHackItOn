Здесь я работаю как календарь вашей команды. По умолчанию всё просто: событие видно *только этой группе*, а запись *свободная*.

*Главные команды*
/gevents — мероприятия этой группы
/gnew — создать мероприятие
/ginvite — пригласить людей в уже созданное событие
/ghelp — эта помощь

Можно также тегнуть меня:
`@meventur_bot мероприятия`
`@meventur_bot создать мероприятие ...`

