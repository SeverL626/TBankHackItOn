*Meventus: режим группы*

Здесь я работаю как календарь вашей команды. Показываю и создаю мероприятия *только этого чата*, а не всю общую афишу.

*Главные команды*
/gevents — мероприятия этой группы
/gnew — создать мероприятие группы
/ghelp — эта помощь

Можно также тегнуть меня:
`@meventur_bot мероприятия`
`@meventur_bot создать мероприятие ...`

*Создать со свободной записью*
