*Meventur: личный режим*

Это режим для тебя лично: найти мероприятие, записаться, создать своё, управлять своими событиями.

*Найти и записаться*
Нажми *🔎 Найти* или введи /events.
Я пришлю карточки. В карточке:
• *🔍 Подробнее* — детали
• *✅ Участвовать* — записаться
• *❌ Покинуть* — выйти

*Создать*
Нажми *➕ Создать* или введи /new.
Приватное: /new private
Я задам вопросы по шагам.

*Мои события*
Нажми *👤 Мои* или введи /my.
Там будут события, где ты участник или организатор.

*Mini App*
Нажми *🌐 Mini App* или /stats.
Там удобнее создавать, редактировать и делать рассылки.

*Команды*
/events — афиша
/new — создать
/my — мои события
/broadcast — рассылка участникам
/stats — Mini App
/cancel — отменить текущий сценарий

Для группы добавь меня в чат и напиши /ghelp.
"""

