`/gnew Демо-день | 25.05.2026 18:00 | офис`

Я создам событие этой группы, и люди смогут нажать *✅ Участвовать*.

*Если нужно пригласить конкретных людей*
Сначала создай событие, потом:
`/ginvite 12 @user1 @user2`

ID события виден в списке /gevents.

