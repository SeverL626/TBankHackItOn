Тут всё просто:
1. Нажми *🔎 Найти* — увидишь карточки мероприятий.
2. В карточке нажми *✅ Участвовать*.
3. Свои записи смотри в *👤 Мои*.

Организатор? Нажми *➕ Создать* — я проведу по шагам.
""".trimIndent()

